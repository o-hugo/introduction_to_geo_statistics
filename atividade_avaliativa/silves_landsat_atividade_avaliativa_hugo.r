  ##--cálculo de índices espectrais--##
  Imagens de satélite são uma poderosa ferramenta para 
entender nosso planeta. Com elas, podemos monitorar a vegetação, 
rastrear a expansão urbana e mapear corpos d’água e identificar
pontos de queimadas. Quatro índices espectrais  amplamente 
usados nesse processo são:
  
  ##--NDVI (Normalized Difference Vegetation Index): Mede a densidade da vegetação.
  NDVI = (valor do pixel (nir) - valor do pixel no (red)) / (valor do pixel (nir) + valor do pixel no (red) )

#--O Que os Valores Representam?
Próximo de 1: Vegetação densa e saudável.
Próximo de 0: Solo exposto ou áreas urbanas.
Negativo: Água ou superfícies sem vegetação.
#-Aplicações-#: Monitoramento de florestas, Avaliação da saúde agrícola, Análise de mudanças no uso do solo.

#-NDBI (Normalized Difference Built-up Index): Identifica áreas construídas. Utilizado para a atualização de informações sobre a extensão e a mudança dos espaços urbanos
NDBI = (valor do pixel (S1) - valor do pixel no (nir)) / (valor do pixel (S1) + valor do pixel no (nir) )

#--O Que os Valores Representam?
Positivos: Áreas construídas (asfalto, concreto, etc...).
Negativos: Vegetação ou corpos d’água (acumulo de água).
#-Aplicações-#: Mapeamento de expansão urbana, Análise de impactos ambientais da urbanização.

#-NDWI (Normalized Difference Water Index): Destaca corpos d’água, especialmente em ambientes urbanos.
NDWI = (valor do pixel (green) - valor do pixel no (nir)) / (valor do pixel (green) + valor do pixel no (nir) )

#--O Que os Valores Representam?
Positivos: Presença de água.
Negativos: Áreas urbanas ou vegetação.
#-Aplicações-#: Identificação de igarapés, lagos e rios,
Monitoramento de zonas alagadas e Gestão de recursos hídricos.

#-NBR (Normalized Burn Ratio) -  índice projetado para destacar áreas queimadas em grandes zonas de incêndio.
NBR = (valor do pixel (nir) - valor do pixel no (S1)) / (valor do pixel (nir) + valor do pixel no (S1) )
#--O Que os Valores Representam?
Positivos: Área Queimada.
Negativos: Áreas não queimadas ou corpos d’água. 
#-Aplicações-#: Monitoramento da sobrevida da vegetação após a queima,
Análise da gravidade de queimadas, Detecção de incêndios florestais ativos.


##--função genérica para calcular NDVI, NDBI, NDWI
index <- function(img, k, i) {
  bk <- img[[k]]
  bi <- img[[i]]
  index <- (bk - bi) / (bk + bi)
  return(index)
}

ndvi <- index(manaus_landsat_rast, 5, 4)
